# Program to add natural numbers up to n
# sum = 1+2+3+...+n

# To take input from the user:
# n = int(input("Enter n: "))
n = 10

# Initialize sum and counter
sum = 0
i = 1

while i <= n:
    sum = sum + i  # Indented correctly
    i = i + 1  # Indented correctly

# Print the sum
print("The sum is", sum)
